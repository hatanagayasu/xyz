function print_big5($hi_start, $hi_end, $lo1_start, $lo1_end,
  $lo2_start, $lo2_end)
{
  if($lo1_start)
  {
    printf("\n0x%02x-0x%02x 0x%02x-0x%02x\n", $hi_start, $hi_end,
      $lo1_start, $lo1_end);
    for($i = $hi_start; $i <= $hi_end; $i++)
      for($j = $lo1_start; $j <= $lo1_end; $j++)
        if(!@iconv("big5", "utf-8", sprintf("%c%c", $i, $j)))
          printf("%02x%02x, ", $i, $j);
  }
  if($lo2_start)
  {
    printf("\n0x%02x-0x%02x 0x%02x-0x%02x\n", $hi_start, $hi_end,
      $lo2_start, $lo2_end);
    for($i = $hi_start; $i <= $hi_end; $i++)
      for($j = $lo2_start; $j <= $lo2_end; $j++)
        if(!@iconv("big5", "utf-8", sprintf("%c%c", $i, $j)))
          printf("%02x%02x, ", $i, $j);
  }
}
