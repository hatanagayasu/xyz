#!/usr/bin/perl

print_big5(0xa1, 0xa2, 0x40, 0x7e, 0xa1, 0xfe);
print_big5(0xa3, 0xa3, 0x40, 0x7e, 0xa1, 0xbf);
print_big5(0xa3, 0xa3, 0x00, 0x00, 0xe1, 0xe1);
print_big5(0xa4, 0xc5, 0x40, 0x7e, 0xa1, 0xfe);
print_big5(0xc6, 0xc6, 0x40, 0x7e, 0x00, 0x00);
print_big5(0xc6, 0xc6, 0x00, 0x00, 0xa1, 0xfe);
print_big5(0xc7, 0xc8, 0x40, 0x7e, 0xa1, 0xfe);
print_big5(0xc9, 0xf8, 0x40, 0x7e, 0xa1, 0xfe);
print_big5(0xf9, 0xf9, 0x40, 0x7e, 0xa1, 0xd5);
print_big5(0xf9, 0xf9, 0x00, 0x00, 0xd6, 0xdc);
print_big5(0xf9, 0xf9, 0x00, 0x00, 0xdd, 0xfe);

sub print_big5
{
  my ($hi_start, $hi_end, $lo1_start, $lo1_end, $lo2_start, $lo2_end) = @_;

  if($lo1_start)
  {
    printf("0x%02x-0x%02x 0x%02x-0x%02x\n", $hi_start, $hi_end, $lo1_start, $lo1_end);
    $n = 0;
    for($i = $hi_start; $i <= $hi_end; $i++)
    {
      for($j = $lo1_start; $j <= $lo1_end; $j++)
      {
        printf("%c%c", $i, $j, $i, $j);
        $n++;
        printf("\n") if($n % 30 == 0);
      }
    }
    printf("\n");
  }
  if($lo2_start)
  {
    printf("0x%02x-0x%02x 0x%02x-0x%02x\n", $hi_start, $hi_end, $lo2_start, $lo2_end);
    $n = 0;
    for($i = $hi_start; $i <= $hi_end; $i++)
    {
      for($j = $lo2_start; $j <= $lo2_end; $j++)
      {
        printf("%c%c", $i, $j, $i, $j);
        $n++;
        printf("\n") if($n % 30 == 0);
      }
    }
    printf("\n");
  }
}
